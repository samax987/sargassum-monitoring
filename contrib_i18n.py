def current_lang() -> str:
    """Langue courante via ?lang= (mémorisée en session), sinon session, repli FR.

    Centralise la logique pour la carte (sargassum_web) et le portail
    (sargassum_contributor_routes).
    """
    from flask import request, session
    q = (request.args.get("lang") or "").lower()
    if q in SUPPORTED_LANGS:
        session["lang"] = q
        return q
    return session.get("lang", DEFAULT_LANG)
